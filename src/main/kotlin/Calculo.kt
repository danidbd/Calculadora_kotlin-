package org.example

class Calculo {
    public fun soma(a:Int, b:Int):Int{
        return (a+b)
    }

    public fun subtracao(a:Int, b:Int):Int{
        return (a-b)
    }

    public fun divisao(a:Int, b:Int):Int{
        return (a/b)
    }

    public fun multiplicacao(a:Int, b:Int):Int{
        return (a*b)
    }

}